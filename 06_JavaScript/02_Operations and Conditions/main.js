// 10 Examples of Operations and Conditions in JavaScript

// 1. Addition
let sum = 5 + 3;
console.log("1. Addition: 5 + 3 =", sum);

// 2. Subtraction
let difference = 10 - 4;
console.log("2. Subtraction: 10 - 4 =", difference);

// 3. Multiplication
let product = 6 * 7;
console.log("3. Multiplication: 6 * 7 =", product);

// 4. Division
let quotient = 20 / 5;
console.log("4. Division: 20 / 5 =", quotient);

// 5. Modulus (Remainder)
let remainder = 17 % 3;
console.log("5. Modulus: 17 % 3 =", remainder);

// 6. Increment
let count = 10;
count++;
console.log("6. Increment: 10++ =", count);

// 7. Decrement
let value = 5;
value--;
console.log("7. Decrement: 5-- =", value);

// 8. Equality Condition
let isEqual = (5 == '5');
console.log("8. Equality (5 == '5'):", isEqual);

// 9. Strict Equality Condition
let isStrictEqual = (5 === '5');
console.log("9. Strict Equality (5 === '5'):", isStrictEqual);

// 10. If-Else Condition
let age = 18;
if (age >= 18) {
    console.log("10. If-Else: You are an adult.");
} else {
    console.log("10. If-Else: You are not an adult.");
}
// 11. If-Else Condition

let temperature = 25;
if (temperature > 30) {
    console.log("The weather is hot.");
} else if (temperature >= 20) {
    console.log("The weather is warm.");
} else {
    console.log("The weather is cold.");
}
