// Variable declarations and their data types in JavaScript

// String
let name = "Shajar"; // String data type

// Number
let age = 25; // Number data type

// Boolean
let isStudent = true; // Boolean data type

// Undefined
let address; // Undefined data type

// Null
let car = null; // Null data type

// Object
let person = {
    firstName: "Shajar",
    lastName: "Abbas"
}; // Object data type

// Array
let colors = ["red", "green", "blue"]; // Array (object) data type

// Output the variables to the console
console.log("Name:", name);
console.log("Age:", age);
console.log("Is Student:", isStudent);
console.log("Address:", address);
console.log("Car:", car);
console.log("Person:", person);
console.log("Colors:", colors);

